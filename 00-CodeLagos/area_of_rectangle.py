# Area of rectangle
print('Hiya! Let us calculate the area of a rectangle');

print('The area of a rectangle is calculated as the product of the length and breadth of the rectangle');

print('Kindly provide us with your values for length and breadth of your rectangle');

rect_length = input('Enter the length of your rectangle: ');
rect_breadth = input('Enter the breadth of your rectangle: ');

area_of_rectangle = float(rect_length) * float(rect_breadth);
print('Area of your rectangle is: ', area_of_rectangle);
