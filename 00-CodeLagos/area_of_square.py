# Area of square
print('Hiya! Let us calculate the area of a square');

print('The area of a square is calculated as the length of the square raised to the power of 2');

print('Kindly provide us with the value for length of your square');

sq_length = input('Enter the length of your square: ');

area_of_square = float(sq_length) ** 2;
print('Area of your square is: ', area_of_square);
